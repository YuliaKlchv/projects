public class Resolution {
    private int bright;
    private int height;

    public Resolution(int bright, int height) {
        this.bright = bright;
        this.height = height;
    }

    public int getBright() {
        return bright;
    }

    public void setBright(int bright) {
        this.bright = bright;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
